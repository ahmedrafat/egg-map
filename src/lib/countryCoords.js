// Resolve a country string/code to [lat, lng] — forgiving about case and aliases.
export function resolveCountry(input) {
  if (!input) return null
  const s = String(input).trim()
  if (!s) return null
  // Try ISO-2 first (uppercase)
  const up = s.toUpperCase()
  if (COUNTRY_COORDS[up]) return { code: up, coords: COUNTRY_COORDS[up] }
  // Try alias lookup
  const lower = s.toLowerCase()
  const alias = COUNTRY_ALIASES[lower]
  if (alias && COUNTRY_COORDS[alias]) return { code: alias, coords: COUNTRY_COORDS[alias] }
  return null
}
